class Solution
{
    //Function to find the maximum money the thief can get.
    public int FindMaxSum(int nums[], int n)
    {
        // Your code here
        int prev1=0;
        int prev2=nums[0];

        for(int i=2;i<nums.length+1;i++){
            int curr= Math.max(nums[i-1]+prev1,prev2);
            prev1=prev2;
            prev2=curr;
        }


        return prev2;
    }
}